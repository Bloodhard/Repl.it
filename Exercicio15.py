n1=float(input('Digite o Primeiro número: '))
n2=float(input('Digite o Segundo número: '))

op=input('Digite a operação desejada: ')
if op == '1':
  print(n1+n2)
elif op == '2':
  print(n1-n2)
elif op == '3':
  print(n1*n2)
elif op == '4':
  print(n1/n2)
elif op == '5':
  n1 < n2
  print(n2)
elif op == '6':
  n1 > n2
  print(n1)


